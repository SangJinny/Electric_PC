package electricity;

public class MonthValue {

	public String P_ID;
	public String name;
	public String three_Monthvalue;
	public String two_Monthvalue;
	public String one_Monthvalue;
	public String Day_after;
	public String Monthvalue;
	public String getP_ID() {
		return P_ID;
	}
	public void setP_ID(String p_ID) {
		P_ID = p_ID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getThree_Monthvalue() {
		return three_Monthvalue;
	}
	public void setThree_Monthvalue(String three_Monthvalue) {
		this.three_Monthvalue = three_Monthvalue;
	}
	public String getTwo_Monthvalue() {
		return two_Monthvalue;
	}
	public void setTwo_Monthvalue(String two_Monthvalue) {
		this.two_Monthvalue = two_Monthvalue;
	}
	public String getOne_Monthvalue() {
		return one_Monthvalue;
	}
	public void setOne_Monthvalue(String one_Monthvalue) {
		this.one_Monthvalue = one_Monthvalue;
	}
	public String getDay_after() {
		return Day_after;
	}
	public void setDay_after(String day_after) {
		Day_after = day_after;
	}
	public String getMonthvalue() {
		return Monthvalue;
	}
	public void setMonthvalue(String monthvalue) {
		Monthvalue = monthvalue;
	}
	
	
	
}
